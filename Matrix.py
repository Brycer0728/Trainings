# Matrix - is a way to describe multidimensional list
matrix = [
    [1,2,3],
    [2,4,6],
    [7,8,9]
]

print(matrix[0][1]) # this line of code prints '2'
# the first number in the brackets represent which list we are accessing, there are 3 list so 0, 1, and 2 are the possibilities
# the list '0' is '[1,2,3]'
# the second number in brackets represents the data in the list, starts at 0 and goes to 2, 1 is the number in the middle so it is '2'